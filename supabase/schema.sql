alter table public.documents add column if not exists designer_id integer references public.designers(id) on delete restrict;
alter table public.admin_users add column if not exists branch_id integer references public.branches(id) on delete restrict;
alter table public.admin_users add column if not exists role text default 'admin';
alter table public.sessions add column if not exists branch_id integer references public.branches(id) on delete set null;
alter table public.sessions add column if not exists role text default 'admin';
alter table public.sessions add column if not exists is_master boolean default false;

create unique index if not exists designers_branch_id_name_key on public.designers(branch_id, name);
create index if not exists templates_branch_id_idx on public.templates(branch_id);
create index if not exists documents_branch_id_idx on public.documents(branch_id);
create index if not exists documents_designer_id_idx on public.documents(designer_id);
create index if not exists documents_template_id_idx on public.documents(template_id);
create index if not exists documents_created_at_idx on public.documents(created_at desc);
create index if not exists admin_users_branch_id_idx on public.admin_users(branch_id);
create index if not exists admin_users_kakao_user_id_idx on public.admin_users(kakao_user_id);
