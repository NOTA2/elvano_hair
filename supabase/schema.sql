create index if not exists login_attempt_logs_attempted_at_idx on public.login_attempt_logs(attempted_at desc);
create index if not exists login_attempt_logs_kakao_user_id_idx on public.login_attempt_logs(kakao_user_id);
create index if not exists sessions_expires_at_idx on public.sessions(expires_at);

